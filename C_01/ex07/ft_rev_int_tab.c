void	ft_rev_int_tab(int *tab, int size)
{
	int	k;
	int	i;

	i = 0;
	while (i < size / 2)
	{
		k = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = k;
		i++;
	}
}
